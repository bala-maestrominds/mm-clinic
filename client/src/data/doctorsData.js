// Get all specializations
export const getSpecializations = () => {
  const specializations = doctorsData.map(doc => doc.specialization);
  return ['All Specializations', ...new Set(specializations)];
};

// Get doctor by ID
export const getDoctorById = (id) => {
  return doctorsData.find(doctor => doctor.id === id) || null;
};

// Filter doctors by specialization
export const filterDoctors = (specialization, searchTerm = '') => {
  let filtered = doctorsData;
  
  if (specialization && specialization !== 'All Specializations') {
    filtered = filtered.filter(doc => doc.specialization === specialization);
  }
  
  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(doc => 
      doc.name.toLowerCase().includes(term) ||
      doc.specialization.toLowerCase().includes(term)
    );
  }
  
  return filtered;
};